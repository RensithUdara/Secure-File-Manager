const { contextBridge, ipcRenderer } = require('electron');

contextBridge.exposeInMainWorld('vault', {
    getProfiles: () => ipcRenderer.invoke('profiles:list'),
    createProfile: (payload) => ipcRenderer.invoke('profiles:create', payload),
    openProfile: (payload) => ipcRenderer.invoke('profiles:open', payload),
    updateProfile: (payload) => ipcRenderer.invoke('profiles:update', payload),
    listEntries: (payload) => ipcRenderer.invoke('entries:list', payload),
    createFolder: (payload) => ipcRenderer.invoke('entries:createFolder', payload),
    deleteEntry: (payload) => ipcRenderer.invoke('entries:delete', payload),
    renameEntry: (payload) => ipcRenderer.invoke('entries:rename', payload),
    toggleLock: (payload) => ipcRenderer.invoke('entries:toggleLock', payload),
    openEntry: (payload) => ipcRenderer.invoke('entries:open', payload),
    importFiles: (payload) => ipcRenderer.invoke('entries:importFiles', payload),
    previewEntry: (payload) => ipcRenderer.invoke('entries:preview', payload),
    searchEntries: (payload) => ipcRenderer.invoke('entries:search', payload),
    listRecent: (payload) => ipcRenderer.invoke('entries:recent', payload),
    listTrash: (payload) => ipcRenderer.invoke('entries:listTrash', payload),
    restoreTrash: (payload) => ipcRenderer.invoke('entries:restoreTrash', payload),
    purgeTrash: (payload) => ipcRenderer.invoke('entries:purgeTrash', payload),
    getEntryMeta: (payload) => ipcRenderer.invoke('entries:getMeta', payload),
    setEntryMeta: (payload) => ipcRenderer.invoke('entries:setMeta', payload),
    createVersion: (payload) => ipcRenderer.invoke('entries:createVersion', payload),
    listVersions: (payload) => ipcRenderer.invoke('entries:listVersions', payload),
    restoreVersion: (payload) => ipcRenderer.invoke('entries:restoreVersion', payload),
    toggleFavorite: (payload) => ipcRenderer.invoke('favorites:toggle', payload),
    listFavorites: (payload) => ipcRenderer.invoke('favorites:list', payload),
    listActivity: (payload) => ipcRenderer.invoke('activity:list', payload),
    exportActivity: (payload) => ipcRenderer.invoke('activity:export', payload),
    exportVaultArchive: (payload) => ipcRenderer.invoke('vault:exportArchive', payload),
    lockSession: (payload) => ipcRenderer.invoke('session:lock', payload),
    getSettings: () => ipcRenderer.invoke('settings:get'),
    setTheme: (payload) => ipcRenderer.invoke('settings:setTheme', payload),
    setStartupPassword: (payload) => ipcRenderer.invoke('settings:setStartupPassword', payload),
    clearStartupPassword: () => ipcRenderer.invoke('settings:clearStartupPassword'),
    unlockStartup: (payload) => ipcRenderer.invoke('settings:unlockStartup', payload),
    createInvite: (payload) => ipcRenderer.invoke('invites:create', payload),
    redeemInvite: (payload) => ipcRenderer.invoke('invites:redeem', payload),
    openCmd: (payload) => ipcRenderer.invoke('system:openCmd', payload),
});
